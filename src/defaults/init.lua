-- ==============================================================================
-- 2. ГЛОБАЛЬНЫЙ РОУТЕР АНИМАЦИЙ
-- ==============================================================================
-- Ядро Wayland вызывает этот хук каждый кадр. Механизм автоматически находит
-- нужную сцену для текущего экрана и перенаправляет вызов в её функцию on_frame().
core.on_frame(function(dt, output_name)
    -- Защита от скачков времени (например, при выходе из спящего режима)
    if dt > 0.1 then dt = 0.016 end 
    
    -- Находим конфигурацию для монитора (или берем фоллбэк "*")
    local out_conf = core.outputs[output_name] or core.outputs["*"]
    
    -- Если у сцены есть логика анимации, вызываем её.
    -- Мы передаем out_conf первым аргументом (self), чтобы сцена могла 
    -- модифицировать свой собственный изолированный state.
    if out_conf and type(out_conf.on_frame) == "function" then
        out_conf.on_frame(out_conf, dt, output_name)
    end
end)

